export type TKey = keyof typeof T.az;

export function useT() {
  // Client-side only hook
  if (typeof window === 'undefined') return (k: TKey) => T.az[k];
  const lang = (document.documentElement.getAttribute('data-lang') as Lang) || 'az';
  return (k: TKey): string => (T[lang] as Record<string, string>)[k] || T.az[k];
}

export function t(lang: Lang, k: TKey): string {
  return (T[lang] as Record<string, string>)[k] || T.az[k];
}

export default T;
